    public function index()
    {
        $user = Auth::user();

        // Retrieve or create display settings for this user
        $profile = Tampilan::firstOrCreate(
            ['user_id' => $user->id],
            [
                'language' => 'id',
                'text_size' => 'default',
                'font_family' => 'Default',
                'dark_mode' => false,
            ]
        );

        return view('profile', compact('user', 'profile'));
    }

